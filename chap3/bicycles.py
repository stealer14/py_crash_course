#Creating and printing elements of a list.
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)

#Accessing elements in a list.
print(bicycles[0]) #accessing first element of list
print(bicycles[0].title()) #string methods are applicable to list elements as str
print(bicycles[1])
print(bicycles[3])
print(bicycles[-1]) #print last item